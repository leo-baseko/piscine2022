#include "ft_rush02.h"

int	ft_isspace(int c)
{
	if (c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\r'
		|| c == ' ')
		return (CHECK_OK);
	return (CHECK_ERR);
}
