void	ft_putchar(char left, char center, char right, int x);

void	rush(int x, int y)
{
	if (x <= 0 || y <= 0)
	{
		return ;
	}
	ft_putchar(111, 45, 111, x);
	if ((y - 2) >= 0)
	{
		while (y > 2)
		{
			ft_putchar(124, 32, 124, x);
			y--;
		}
		ft_putchar(111, 45, 111, x);
	}
}
