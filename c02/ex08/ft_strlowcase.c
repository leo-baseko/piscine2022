void	ft_change_to_lo(char *a)
{
	if (*a >= 'A' && *a <= 'Z')
		*a = *a + 32;
}

char	*ft_strlowcase(char *str)
{
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		ft_change_to_lo(&str[j]);
		j++;
	}
	return (str);
}
