void	ft_change_to_up(char *a)
{
	if (*a >= 'a' && *a <= 'z')
		*a = *a - 32;
}

char	*ft_strupcase(char *str)
{
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		ft_change_to_up(&str[j]);
		j++;
	}
	return (str);
}
